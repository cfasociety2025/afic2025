// Group speakers by country
export const getSpeakersByCountry = () => {
  const grouped = {};

  speakersData.forEach((speaker) => {
    const country = speaker.location;
    if (!grouped[country]) {
      grouped[country] = [];
    }
    grouped[country].push(speaker);
  });

  // Define country order with Kenya first
  const countryOrder = [
    "Kenya",
    "Kenya/Sweden",
    "Nigeria",
    "South Africa",
    "Zimbabwe",
    "Uganda",
    "Ethiopia",
    "Egypt",
    "Mauritius",
    "Somalia",
    "Canada",
    "United Kingdom",
  ];

  // Sort countries according to the order, putting any unlisted countries at the end
  const sortedCountries = Object.keys(grouped).sort((a, b) => {
    const indexA = countryOrder.indexOf(a);
    const indexB = countryOrder.indexOf(b);

    if (indexA === -1 && indexB === -1) return a.localeCompare(b);
    if (indexA === -1) return 1;
    if (indexB === -1) return -1;

    return indexA - indexB;
  });

  const sortedGrouped = {};
  sortedCountries.forEach((country) => {
    sortedGrouped[country] = grouped[country];
  });

  return sortedGrouped;
};

// Group speakers with special sections for CFA Institute and CFA Society at the top
export const getSpeakersGrouped = () => {
  const cfaInstitute = [];
  const cfaSociety = [];
  const westernAfrica = [];
  const southernAfrica = [];
  const eastAfrica = [];
  const otherCountries = [];

  // Define regional groupings
  const westernAfricaCountries = ["Nigeria", "Ghana"];
  const southernAfricaCountries = ["South Africa", "Zimbabwe"];
  const eastAfricaCountries = ["Kenya", "Kenya/Sweden", "Uganda", "Ethiopia", "Somalia", "Rwanda"];

  speakersData.forEach((speaker) => {
    // CFA Society and CFA Institute take precedence - no speaker appears in multiple groups
    if (speaker.company && speaker.company.includes("CFA Society")) {
      cfaSociety.push(speaker);
    } else if (speaker.company && speaker.company.includes("CFA Institute")) {
      cfaInstitute.push(speaker);
    } else {
      // Group remaining speakers by region based on location
      const country = speaker.location;
      
      if (westernAfricaCountries.includes(country)) {
        westernAfrica.push(speaker);
      } else if (southernAfricaCountries.includes(country)) {
        southernAfrica.push(speaker);
      } else if (eastAfricaCountries.includes(country)) {
        eastAfrica.push(speaker);
      } else {
        otherCountries.push(speaker);
      }
    }
  });

  // Build the final grouped object in the specified order
  const result = [];

  // 1. CFA Institute
  if (cfaInstitute.length > 0) {
    result.push({
      section: "CFA Institute",
      speakers: cfaInstitute,
    });
  }

  // 2. CFA Society
  if (cfaSociety.length > 0) {
    result.push({
      section: "CFA Society",
      speakers: cfaSociety,
    });
  }

  // 3. West Africa
  if (westernAfrica.length > 0) {
    result.push({
      section: "West Africa",
      speakers: westernAfrica,
    });
  }

  // 4. Southern Africa
  if (southernAfrica.length > 0) {
    result.push({
      section: "Southern Africa",
      speakers: southernAfrica,
    });
  }

  // 5. East Africa
  if (eastAfrica.length > 0) {
    result.push({
      section: "East Africa",
      speakers: eastAfrica,
    });
  }

  // 6. Other Countries
  if (otherCountries.length > 0) {
    result.push({
      section: "Other Countries",
      speakers: otherCountries,
    });
  }

  // 7. Moderators & MCs (empty for now, will be populated later)
  result.push({
    section: "Moderators & MCs",
    speakers: [],
  });

  return result;
};
