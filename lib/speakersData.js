// Group speakers by country
export const getSpeakersByCountry = () => {
  const grouped = {};

  speakersData.forEach((speaker) => {
    const country = speaker.location;
    if (!grouped[country]) {
      grouped[country] = [];
    }
    grouped[country].push(speaker);
  });

  // Define country order with Kenya first
  const countryOrder = [
    "Kenya",
    "Kenya/Sweden",
    "Nigeria",
    "South Africa",
    "Zimbabwe",
    "Uganda",
    "Ethiopia",
    "Egypt",
    "Mauritius",
    "Somalia",
    "Canada",
    "United Kingdom",
  ];

  // Sort countries according to the order, putting any unlisted countries at the end
  const sortedCountries = Object.keys(grouped).sort((a, b) => {
    const indexA = countryOrder.indexOf(a);
    const indexB = countryOrder.indexOf(b);

    if (indexA === -1 && indexB === -1) return a.localeCompare(b);
    if (indexA === -1) return 1;
    if (indexB === -1) return -1;

    return indexA - indexB;
  });

  const sortedGrouped = {};
  sortedCountries.forEach((country) => {
    sortedGrouped[country] = grouped[country];
  });

  return sortedGrouped;
};

// Group speakers based on their 'group' field
export const getSpeakersGrouped = () => {
  // Create a map to hold speakers by their group
  const groupMap = {};

  // Group speakers by their 'group' field
  speakersData.forEach((speaker) => {
    let group = speaker.group || "Other Countries"; // Default to "Other Countries" if no group specified
    
    // Consolidate "moderator" and "mc" into "Moderators & MCs"
    if (group.toLowerCase() === "moderator" || group.toLowerCase() === "mc") {
      group = "Moderators & MCs";
    }
    
    if (!groupMap[group]) {
      groupMap[group] = [];
    }
    groupMap[group].push(speaker);
  });

  // Define the order of sections
  const sectionOrder = [
    "CFA Institute",
    "CFA Society",
    "West Africa",
    "Egypt",
    "Southern Africa",
    "East Africa",
    "Other Countries",
    "Moderators & MCs"
  ];

  // Build the final grouped array in the specified order
  const result = [];

  sectionOrder.forEach((section) => {
    if (groupMap[section] && groupMap[section].length > 0) {
      result.push({
        section: section,
        speakers: groupMap[section],
      });
    }
  });

  return result;
};
